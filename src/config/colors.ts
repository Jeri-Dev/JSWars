export const generateCssVars = (): string => {
	const vars = Object.entries(APP_COLORS)
		.map(([key, value]) => {
			const cssVar = `--${key.replace(/_/g, "-").toLowerCase()}`
			return `${cssVar}: ${value};`
		})
		.join("\n  ")

	return `:root {\n  ${vars}\n}`
}
